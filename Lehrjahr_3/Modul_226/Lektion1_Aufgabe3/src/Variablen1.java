public class Variablen1 {

	public static void main(String[] args) {
		int i = 3;
		float f = 5.5f;
		char c = 'A';
		boolean b = true;
		
		i = (int)2.5;
		f = 3;
		c = '1';
		b = false;
		
		System.out.println(i);
		System.out.println(f);
		System.out.println(c);
		System.out.println(b);
	}
}